<h2>Introduction</h2>

<p>Foxtrot is a small but powerful framework for using threads with the Java<sup><font="-2">TM</font></sup>
Foundation Classes (JFC/Swing).</p>
<p>It is compatible with the J2SE<sup><font="-2">TM</font></sup>, but can be adapted to work with the
Java<sup><font="-2">TM</font></sup> Development Kit (JDK) version 1.1.x with a small modification to an import
statement.<br>
This is due to the fact that class ActiveEvent moved from package java.awt.peer in JDK 1.1.x to package
java.awt in J2SE.</p>
<p>The Foxtrot framework is released under the BSD license.</p>

